</p>
<p>
    <b>5.5.</b> Оплата Услуг также может производиться иными способами, доступными в личном кабинете Заказчика Сервиса «Робофид».
</p>
<p>
    <b>5.6.</b> Услуги считаются оплаченными Заказчиком с момента получения Робофидом подтверждения из банка о поступлении всей суммы оплаты на расчетный счет Робофида. В отдельных случаях по собственному усмотрению Робофида подтверждением факта оплаты может служить:<br/>
    а) факсимильная копия платежного поручения с отметкой банка об оплате при безналичной форме оплаты;<br/>
    б) факсимильная копия квитанции об оплате с печатью банка, через который произведена оплата;<br/>
    в) верификация Робофидом факта платежа в пользу Робофидом через платежные системы в случае осуществления Заказчиком электронного платежа.
</p>
<p>
