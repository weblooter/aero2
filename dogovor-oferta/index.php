<p>
    <b>4.1.4.</b> В случае отказа Заказчика от исполнения Договора по основаниям, предусмотренным Офертой, возвратить Заказчику по его письменному требованию сумму предоплаты за вычетом стоимости фактически оказанных Робофидом к моменту расторжения Договора Услуг, а так сопутствующих и понесеных расходов. Возврат осуществляется в течение десяти рабочих дней с момента получения Робофидом надлежаще оформленного письменного требования Заказчика и в необходимых случаях иных документов, идентифицирующих Заказчика.
