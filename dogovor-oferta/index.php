<p>
    <b>4.2.4.</b> Приостановить оказание Услуг и/или расторгнуть Договор(-ы) с Заказчиком в одностороннем порядке в случае, если Заказчик имеет задолженность по оплате Услуг «Робофид» (в частности нарушил срок оплаты Услуг, оказанных Заказчику по Договору, заключенному на условиях отсрочки платежа) как по договорам, заключенным на условиях настоящей Оферты, так и по другим договорам, заключенным между Робофид и Заказчиком.
</p>
<p>
