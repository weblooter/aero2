    а) нарушения Заказчиком обязательства, предусмотренного <b>п.п.4.3</b> Оферты;<br/>
    б) в случае нарушения предоставления Заказчиком Robofeed XML, содержимое которого прямо или косвенно нарушает законодательство РФ;<br/>
    в) при нарушении Заказчиком иных обязательств, принятых в соответствии с Договором;<br/>
    г) при попытке Заказчика получить несанкционированный доступ к непредоставленному и/или непубличному функционалу Сервиса «Робофид»;
