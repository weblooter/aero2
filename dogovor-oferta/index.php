</p>
<p>
    <b>3.1.2.</b> Заказчик произвел предоплату в сумме не меньше необходимой, согласно его тарифному плану;
</p>
<p>
    <b>3.1.3.</b> Заказчик осуществил Акцепт Оферты.
</p>
<p>
    <b>3.2.</b> Заказчик самостоятельно осуществляет подготовку Robofeed XML и предоставление их Работу посредством размещения в личном кабинете Сервиса «Робофид». Заказчик самостоятельно и в полном объеме несет предусмотренную законодательством ответственность за содержимое Robofeed XML и гарантирует соответствие составленных и предоставленных Robofeed XML всем требованиям действующего законодательства РФ.
</p>
<p>
    <b>3.3.</b> Иные согласованные Сторонами сведения могут предоставляться Заказчиком через Веб-интерфейс Робофид.
</p>
<p>
    <b>3.4.</b> В случае если Robofeed XML предоставлены Заказчиком с нарушением правил и требований Договора (в т.ч. Общих Условий), Робофид вправе отказать в обработке таких Robofeed XML, приостановить или прекратить оказание Услуг в отношении таких Robofeed XML, расторгнуть Договор.
</p>
<p>
    <b>3.5.</b> Заказчик не вправе передавать свои права по Договору какой-либо третьей стороне без предварительного согласия Робофид. Обязанность подтверждения наличия такого согласия возлагается на Заказчика.
</p>
<p>
    <b>3.6.</b> Услуги предоставляются Заказчику в объеме, соответствующем сумме произведенной им предоплаты (если Офертой не предусмотрено иное). Робофид оставляет за собой право немедленно приостановить оказание Услуг и/или расторгнуть Договор в одностороннем порядке в случае, если стоимость Услуг, оказанных Робофид по Договору, стала равной или превысила сумму, перечисленную Заказчиком на расчетный счет Робофид в качестве предоплаты за оказание Услуг.
</p>
<p>
    <b>3.7.</b> Заказчик самостоятельно несет ответственность за сохранность и конфиденциальность регистрационных данных (логин и пароль). Все действия, осуществленные в отношении размещения Robofeed XML через Сервис «Робофид» с использованием логина и пароля Заказчика, считаются осуществленными Заказчиком. Заказчик самостоятельно несет ответственность перед третьими лицами за все действия, совершенные с использованием логина и пароля Заказчика.
