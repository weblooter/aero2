</p>
<p>
    <b>4.1.2.</b> При оказании Услуг по Договору обеспечить возможность ознакомления Заказчика с данными статистики через Сервис «Робофид», при этом Робофид не несет ответственности в случае невозможности временного и/или постоянного ознакомления Заказчика с данными статистики.
</p>
<p>
    <b>4.1.3.</b> Предоставлять Заказчику ссылки на экспортируемые файлы для дальнейшего использования этих ссылок на выбрах Заказчиком Торговых Площадках, при условии соблюдения Заказчиком требований, изложенных в <b>п.3.8.</b> Оферты и выполнения Заказчиком пунктов, изложенных в <b>п.3.1.</b> Оферты.
